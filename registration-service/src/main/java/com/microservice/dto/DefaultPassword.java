package com.microservice.dto;

public class DefaultPassword {
	String email;
	String phone;
	public DefaultPassword() {
		super();
	}
	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
}
